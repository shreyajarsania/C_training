/*
 * Write a program to print following pattern.
 * a. Using multiple printf statements
 * b. Using single printf statement
 * *
 * **
 * ***
 * ****
 * *****
 */
#include "4.h"
int main()
{
	/*
	 * pattern using multiple printf
	 */
	printf("pattern using multiple printf:\n");
	printf("*\n");
	printf("**\n");
	printf("***\n");
	printf("****\n");
	printf("*****\n");
	
	/*
	 * pattern using single printf
	 */
	printf("pattern using single printf: \n*\n**\n***\n****\n*****\n");
	return 0;
}
